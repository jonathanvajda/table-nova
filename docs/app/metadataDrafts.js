/**
 * @file Draft metadata artifact builders for data dictionaries and JSON Schema.
 */

/**
 * @typedef {import('./rdf/schema.js').ColumnSchema} ColumnSchema
 * @typedef {import('./rdf/buildDataset.js').QuadRecord} QuadRecord
 */

const XSD_NS = 'http://www.w3.org/2001/XMLSchema#';
const JSON_SCHEMA_DRAFT_2020_12 = 'https://json-schema.org/draft/2020-12/schema';
const SAMPLE_LIMIT = 5;
const UNIQUE_EXAMPLE_LIMIT = 2;
const INTEGER_TYPES = new Set([
  'byte',
  'int',
  'integer',
  'long',
  'negativeInteger',
  'nonNegativeInteger',
  'nonPositiveInteger',
  'positiveInteger',
  'short',
  'unsignedByte',
  'unsignedInt',
  'unsignedLong',
  'unsignedShort'
]);
const NUMBER_TYPES = new Set(['decimal', 'double', 'float']);

/**
 * Builds both draft metadata export artifacts.
 * @param {{
 *   filename: string,
 *   columnSchemas: ColumnSchema[],
 *   quads?: QuadRecord[],
 *   sampleValuesByPredicate?: Record<string, string[]>
 * }} params
 * @returns {{dataDictionaryCsv: string, jsonSchemaText: string}}
 */
export function buildDraftMetadataArtifacts({
  filename,
  columnSchemas,
  quads = [],
  sampleValuesByPredicate = {}
}) {
  const effectiveSamples = hasSampleValues(sampleValuesByPredicate)
    ? sampleValuesByPredicate
    : collectExampleValuesFromQuads(quads, columnSchemas);

  const rows = buildDataDictionaryRows(columnSchemas, effectiveSamples);
  const jsonSchema = buildJsonSchemaDocument(filename, columnSchemas, effectiveSamples);

  return {
    dataDictionaryCsv: serializeDataDictionaryCsv(rows),
    jsonSchemaText: JSON.stringify(jsonSchema, null, 2)
  };
}

/**
 * Builds compact sample/example values from the first few processed data rows.
 * @param {{rows: string[][], columnSchemas: ColumnSchema[], maxRows?: number}} params
 * @returns {Record<string, string[]>}
 */
export function buildSampleValuesByPredicate({ rows, columnSchemas, maxRows = SAMPLE_LIMIT }) {
  /** @type {Record<string, string[]>} */
  const out = {};
  const sampleRows = (rows || []).slice(0, Math.max(0, maxRows));

  for (const schema of columnSchemas || []) {
    const values = [];

    for (const row of sampleRows) {
      const cell = String(row?.[schema.index] ?? '').trim();
      if (!cell || values.includes(cell)) continue;
      values.push(cell);
      if (values.length >= UNIQUE_EXAMPLE_LIMIT) break;
    }

    if (values.length > 0) {
      out[schema.predicateIri] = values;
    }
  }

  return out;
}

/**
 * Builds dictionary row objects prior to CSV serialization.
 * @param {ColumnSchema[]} columnSchemas
 * @param {Record<string, string[]>} sampleValuesByPredicate
 * @returns {Array<Record<string, string>>}
 */
export function buildDataDictionaryRows(columnSchemas, sampleValuesByPredicate) {
  return (columnSchemas || []).map((schema) => ({
    'data identifier': String(schema?.predicateLocalName || ''),
    'field name': String(schema?.label || schema?.originalHeader || schema?.key || ''),
    datatype: formatDatatypeLabel(schema?.datatypeIri),
    description: '',
    'char length': '',
    format: '',
    example: formatExampleCell(sampleValuesByPredicate?.[schema?.predicateIri] || [])
  }));
}

/**
 * Serializes dictionary rows to CSV.
 * @param {Array<Record<string, string>>} rows
 * @returns {string}
 */
export function serializeDataDictionaryCsv(rows) {
  const headers = [
    'data identifier',
    'field name',
    'datatype',
    'description',
    'char length',
    'format',
    'example'
  ];

  const lines = [
    headers.map(escapeCsvCell).join(','),
    ...(rows || []).map((row) => headers.map((key) => escapeCsvCell(row?.[key] || '')).join(','))
  ];

  return `${lines.join('\n')}\n`;
}

/**
 * Builds a draft JSON Schema document for a tabular row collection.
 * @param {string} filename
 * @param {ColumnSchema[]} columnSchemas
 * @param {Record<string, string[]>} sampleValuesByPredicate
 * @returns {Record<string, any>}
 */
export function buildJsonSchemaDocument(filename, columnSchemas, sampleValuesByPredicate) {
  /** @type {Record<string, any>} */
  const properties = {};

  for (const schema of columnSchemas || []) {
    const descriptor = mapXsdDatatypeToJsonSchema(schema?.datatypeIri);
    const examples = (sampleValuesByPredicate?.[schema?.predicateIri] || [])
      .map((value) => coerceExampleValue(value, schema?.datatypeIri))
      .filter((value) => value !== undefined);

    properties[String(schema?.predicateLocalName || schema?.key || 'field')] = {
      title: String(schema?.label || schema?.originalHeader || schema?.key || 'Field'),
      ...descriptor,
      ...(examples.length > 0 ? { examples } : {})
    };
  }

  return {
    $schema: JSON_SCHEMA_DRAFT_2020_12,
    title: buildJsonSchemaTitle(filename),
    description: `Draft JSON Schema generated by Table Nova for rows from ${String(filename || 'tabular source')}.`,
    type: 'array',
    items: {
      type: 'object',
      properties,
      additionalProperties: false
    }
  };
}

/**
 * Collects up to two unique example values per predicate from stored quad records.
 * @param {QuadRecord[]} quads
 * @param {ColumnSchema[]} columnSchemas
 * @returns {Record<string, string[]>}
 */
export function collectExampleValuesFromQuads(quads, columnSchemas) {
  const predicateSet = new Set((columnSchemas || []).map((schema) => schema?.predicateIri).filter(Boolean));
  /** @type {Record<string, string[]>} */
  const out = {};

  for (const quad of quads || []) {
    const predicate = quad?.p;
    if (!predicateSet.has(predicate)) continue;

    const value = String(quad?.oValue ?? '').trim();
    if (!value) continue;

    const values = out[predicate] || [];
    if (values.includes(value)) continue;

    values.push(value);
    out[predicate] = values.slice(0, UNIQUE_EXAMPLE_LIMIT);
  }

  return out;
}

/**
 * @param {Record<string, string[]>} sampleValuesByPredicate
 * @returns {boolean}
 */
function hasSampleValues(sampleValuesByPredicate) {
  return Object.values(sampleValuesByPredicate || {}).some((values) => Array.isArray(values) && values.length > 0);
}

/**
 * @param {string} value
 * @returns {string}
 */
function escapeCsvCell(value) {
  const s = String(value ?? '');
  return /[",\r\n]/.test(s) ? `"${s.replace(/"/g, '""')}"` : s;
}

/**
 * @param {string[]} values
 * @returns {string}
 */
function formatExampleCell(values) {
  const unique = (values || []).slice(0, UNIQUE_EXAMPLE_LIMIT);
  return unique.join(' | ');
}

/**
 * @param {string} datatypeIri
 * @returns {string}
 */
function formatDatatypeLabel(datatypeIri) {
  const iri = String(datatypeIri || `${XSD_NS}string`);
  return iri.startsWith(XSD_NS) ? `xsd:${iri.slice(XSD_NS.length)}` : iri;
}

/**
 * @param {string} datatypeIri
 * @returns {Record<string, any>}
 */
function mapXsdDatatypeToJsonSchema(datatypeIri) {
  const local = datatypeLocalName(datatypeIri);

  if (INTEGER_TYPES.has(local)) {
    return { type: 'integer' };
  }

  if (NUMBER_TYPES.has(local)) {
    return { type: 'number' };
  }

  if (local === 'boolean') {
    return { type: 'boolean' };
  }

  if (local === 'date') {
    return { type: 'string', format: 'date' };
  }

  if (local === 'dateTime') {
    return { type: 'string', format: 'date-time' };
  }

  if (local === 'time') {
    return { type: 'string', format: 'time' };
  }

  if (local === 'anyURI') {
    return { type: 'string', format: 'uri' };
  }

  return { type: 'string' };
}

/**
 * @param {string} value
 * @param {string} datatypeIri
 * @returns {string|number|boolean|undefined}
 */
function coerceExampleValue(value, datatypeIri) {
  const local = datatypeLocalName(datatypeIri);
  const trimmed = String(value ?? '').trim();
  if (!trimmed) return undefined;

  if (INTEGER_TYPES.has(local)) {
    const parsed = Number(trimmed);
    return Number.isInteger(parsed) ? parsed : trimmed;
  }

  if (NUMBER_TYPES.has(local)) {
    const parsed = Number(trimmed);
    return Number.isFinite(parsed) ? parsed : trimmed;
  }

  if (local === 'boolean') {
    if (trimmed === 'true' || trimmed === '1') return true;
    if (trimmed === 'false' || trimmed === '0') return false;
  }

  return trimmed;
}

/**
 * @param {string} filename
 * @returns {string}
 */
function buildJsonSchemaTitle(filename) {
  const base = String(filename || 'tabular-source').replace(/\.[^.]+$/, '').trim();
  return base ? `${base} rows` : 'Tabular rows';
}

/**
 * @param {string} datatypeIri
 * @returns {string}
 */
function datatypeLocalName(datatypeIri) {
  const iri = String(datatypeIri || `${XSD_NS}string`);
  return iri.startsWith(XSD_NS) ? iri.slice(XSD_NS.length) : '';
}
